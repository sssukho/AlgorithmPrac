#include <iostream>
#include <algorithm>
#define mod 10007
#define MAX 1001

using namespace std;

int cache[MAX][10]; //자리수, 맨 앞자리 시작하는 숫자

int main() {
    cout << "start" << endl;
    int N; //수의 길이
    cin >> N;
    
    for(int i = 0; i < 10; i++)
        cache[1][i] = 1;
    
    for(int i = 2; i <= N; i++) {
        for(int j = 0; j <= 9; j++) {
            for(int k = 0; k <= j; k++) {
                cache[i][j] = cache[i][j] + cache[i-1][k];
            }
            cache[i][j] %= mod;
        }
    }
    
    int sum = 0;
    
    for(int i = 0; i < 10; i++)
        sum = (sum + cache[N][i]) % mod;
    
    cout << sum % mod << endl;
    return 0;
}

